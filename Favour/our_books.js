function calculate(clicks) {
    const quantity = document.getElementById("quantity");
    let intQuantity = parseInt(quantity.innerText);
    const sumvalue = intQuantity + clicks;
    if (sumvalue < 0) {
        sumvalue = 0;
    }

    quantity.innerText = sumvalue;
}